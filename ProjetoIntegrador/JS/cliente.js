function cpf() {
    var cpf = document.getElementById('cpf')
    var cnpj = document.getElementById('cnpj')
    cpf.style.display = 'block'
    cnpj.style.display = 'none'
  }
  
  function cnpj() {
    var cpf = document.getElementById('cpf')
    var cnpj = document.getElementById('cnpj')
    cpf.style.display = 'none'
    cnpj.style.display = 'block'
  }
  